def bubble_sort(arr):
    """
    Сортирует список arr методом пузырька.

    Args:
        arr: Список для сортировки (изменяется на месте).
    """
    n = len(arr)  # Получаем длину списка

    # Внешний цикл: проходим по списку n-1 раз
    # (после каждой итерации наибольший элемент "всплывает" в конец)
    for i in range(n - 1):
        # Оптимизация: если за проход не было обменов, значит список уже отсортирован
        swapped = False

        # Внутренний цикл: сравниваем соседние элементы и меняем их местами,
        # если они находятся в неправильном порядке
        for j in range(n - i - 1):  # Оптимизация: не проверяем уже отсортированные элементы в конце
            if arr[j] > arr[j + 1]:
                # Меняем местами arr[j] и arr[j+1]
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True  # Отмечаем, что был обмен

        # Если не было обменов, список уже отсортирован
        if not swapped:
            break  # Выходим из внешнего цикла


# Пример использования
if __name__ == "__main__":
    my_list = [5, 1, 4, 2, 8]
    print("Исходный список:", my_list)

    bubble_sort(my_list)
    print("Отсортированный список:", my_list)